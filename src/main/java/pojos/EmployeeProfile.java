package pojos;

public class EmployeeProfile {

	Long employeeId;
	String employeeName;
	String employeeEmail;
	Integer employeeRole;
	Long employeeBranch;
	String employeeBranchName;
	
	
	public Long getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(Long employeeId) {
		this.employeeId = employeeId;
	}
	
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	
	public String getEmployeeEmail() {
		return employeeEmail;
	}
	public void setEmployeeEmail(String employeeEmail) {
		this.employeeEmail = employeeEmail;
	}
	
	public Integer getEmployeeRole() {
		return employeeRole;
	}
	public void setEmployeeRole(Integer employeeRole) {
		this.employeeRole = employeeRole;
	}
	
	public Long getEmployeeBranch() {
		return employeeBranch;
	}
	public void setEmployeeBranch(Long employeeBranch) {
		this.employeeBranch = employeeBranch;
	}
	
	public String getEmployeeBranchName() {
		return employeeBranchName;
	}
	public void setEmployeeBranchName(String employeeBranchName) {
		this.employeeBranchName = employeeBranchName;
	}
	
}
